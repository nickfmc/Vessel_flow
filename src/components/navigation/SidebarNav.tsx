export function SidebarNav() {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === '/dashboard') {
      return pathname === '/dashboard';
    }
    return pathname.startsWith(href);
  };

  return (
    <nav className="c-sidebar-nav">
      <div className="c-sidebar-nav__items">
        {navigationItems.map((item) => (
          <Link
            key={item.name}
            href={item.href}
            className={`c-sidebar-nav__item ${
              isActive(item.href) ? 'c-sidebar-nav__item--active' : ''
            }`}
          >
            <div className="c-sidebar-nav__icon">
              {item.icon}
            </div>
            <span className="c-sidebar-nav__text">{item.name}</span>
            {isActive(item.href) && (
              <div className="c-sidebar-nav__indicator" />
            )}
          </Link>
        ))}
      </div>
    </nav>
  );
}